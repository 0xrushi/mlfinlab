"""
An example module with some demo functions
"""


def addition(num1, num2):
    """
    Adds two numbers

    :param num1: numeric 1
    :param num2: numeric 2
    :return: num1 + num2
    """
    return num1 + num2


def subtraction(num1, num2):
    """
    Subtracts 2 numbers

    :param num1: numeric 1
    :param num2: numeric 2
    :return: num1 - num2
    """
    return num1 - num2
